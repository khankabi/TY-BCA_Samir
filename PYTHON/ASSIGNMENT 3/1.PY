# write a python program to add and remove operation on set.
num_set = set([0, 1, 3, 4, 5])
print("Original set:")
print(num_set)

num_set.add(6)
print("\nAfter adding the first element from the said set:")
print(num_set)

num_set.pop()
print("\nAfter removing the first element from the said set:")
print(num_set)
